#!/usr/bin/env python3
"""
Module that defines a function to concatenate two arrays
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays into a new array

    Args:
        arr1 (list): First list of numbers
        arr2 (list): Second list of numbers

    Returns:
        list: A new list containing all elements of arr1 followed by arr2
    """
    return arr1 + arr2
